// [loop 543] Sinh phân tích đa tầng VN từ dữ liệu cổ THẬT (COMBO_DATA)
//   Dùng cho các tổ hợp không có GE_SHI_DEEP tay (tức nhóm 乙~癸).
//   Phân tích: quẻ + 格名 + sao chiếu mệnh + câu thơ + tổng kết.
function generateVN(combo, gua, guaInfo, data, yearNat, hourNat) {
  const interp = interpGeMing(data.gm);
  const xm = data.xm || guaInfo.meaning;
  const lines = [
    `Quẻ ${guaInfo.vi} (${guaInfo.nature}) — cách「${data.gm}」(${interp}).`,
    `Năm ${combo[0]} (${yearNat}) × giờ ${combo[1]} (${hourNat}) → ba sao chiếu mệnh: ${data.st || '—'}.`,
    `Luận cổ (星照命): ${xm}`,
    data.shi ? `Câu thơ cổ: «${data.shi.split('，')[0]}» — mở đầu bức tranh mệnh vận.` : '',
    `Kết luận (述雲): ${data.sy || '—'}`,
    `TỔNG: Phát huy «${guaInfo.nature}», tránh ${guaInfo.vi.includes('Cấn')||guaInfo.vi.includes('Khốn') ? 'sự trì trệ' : 'tính bốc đồng'}. ${guaInfo.meaning}`,
  ].filter(Boolean);
  return lines;
}

/**
 * @param {object} R — kết quả analyze()
 * @returns {{ yearGan, hourGan, combo, gua, guaVi, guaMeaning, geMing, stars, starDesc, geShi, geShiAnalysis, summary }}
 */
export function guiguziFDG(R) {
  const yearGan = R.chart?.pillars?.year?.gan;
  const hourGan = R.chart?.pillars?.time?.gan;
  if (!yearGan || !hourGan) return null;
  const combo = yearGan + hourGan;
  const gua = GUA_MAP[combo] || '震';
  const guaInfo = GUA_VI[gua] || GUA_VI['震'];
  const data = COMBO_DATA[combo] || {};
  const geMing = data.gm || 'Phiêu Bạt';

  const GAN_NATURE = {
    '甲': 'Dương Mộc — tiên phong, trực tiếp, cây lớn', '乙': 'Âm Mộc — linh hoạt, hoa cỏ, mềm mại',
    '丙': 'Dương Hỏa — rực rỡ, nhiệt huyết, mặt trời', '丁': 'Âm Hỏa — ấm áp, đèn soi, tinh tế',
    '戊': 'Dương Thổ — núi lớn, vững chãi, bao dung', '己': 'Âm Thổ — đất ruộng, thực dụng, nuôi trồng',
    '庚': 'Dương Kim — kiếm bén, quyết đoán, dũng cảm', '辛': 'Âm Kim — trang sức, tinh xảo, thẩm mỹ',
    '壬': 'Dương Thủy — biển cả, bao la, bao dung', '癸': 'Âm Thủy — sương móc, thấm nhuần, tinh tế',
  };
  const yearNat = GAN_NATURE[yearGan] || '';
  const hourNat = GAN_NATURE[hourGan] || '';

  // Ưu tiên GE_SHI_DEEP (5-layer tay cho nhóm 甲), else generateVN từ dữ liệu cổ
  const deep = GE_SHI_DEEP[combo];
  let geShi, geShiAnalysis;
  if (deep) {
    geShi = deep.shi;
    geShiAnalysis = deep.analysis;
  } else {
    geShi = data.shi || '';
    geShiAnalysis = generateVN(combo, gua, guaInfo, data, yearNat, hourNat);
  }

  const summary = `${combo} → quẻ ${guaInfo.vi} | cách「${geMing}」| sao: ${data.st || '—'} — ${(data.sy || guaInfo.meaning).slice(0, 60)}`;
  return {
    yearGan, hourGan, combo, gua, guaVi: guaInfo.vi, guaNature: guaInfo.nature,
    guaMeaning: guaInfo.meaning, geMing, stars: data.st || '', starDesc: data.xm || '',
    shuYun: data.sy || '', geShi, geShiAnalysis, summary,
  };
}

